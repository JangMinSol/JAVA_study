public class Variable{
    public static void main(String[] args) {
    	int a;
    	a = 1;
    	System.out.println(a+1); //2
    	System.out.println(a+1.5); //2.5
    	
    	double b;
    	b = 5.1;
    	System.out.println(b+1); //6.1
    	
    	//String course;
    	//course = "Java";
    	String course = "Java";
    	System.out.println(course);
    	
    	String c, d;
    	c = "Python";
    	d = "Coding";
    	System.out.println(c+d); //PythonCoding
    }
}